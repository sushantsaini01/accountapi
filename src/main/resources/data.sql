DROP TABLE IF EXISTS TBL_EMPLOYEES;

CREATE TABLE TBL_EMPLOYEES (
  id INT AUTO_INCREMENT  PRIMARY KEY,
  first_name VARCHAR(250) NOT NULL,
  last_name VARCHAR(250) NOT NULL,
  email VARCHAR(250) DEFAULT NULL
);

DROP TABLE IF EXISTS ACCT_BALANCE;
CREATE TABLE ACCT_BALANCE(
    ACCOUNTNUMBER VARCHAR(16) NOT NULL PRIMARY KEY,
    BALANCE FLOAT (15) NOT NULL,
    TRANSTIMESTAMP TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

DROP TABLE IF EXISTS ACCT_TRANSACTION;
CREATE TABLE ACCT_TRANSACTION(
    TRANSID INT AUTO_INCREMENT  PRIMARY KEY,
    ACCOUNTNUMBER VARCHAR(16) NOT NULL,
    TRANSTIMESTAMP TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    TRANSTYPE VARCHAR (10) NOT NULL,
    AMOUNT FLOAT (15) NOT NULL
);

